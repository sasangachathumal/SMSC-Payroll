    private void loadtable() {
        try {
            ArrayList<Loan> GetAllLoans = new Loan_Controller().GetAllLoans();
            DefaultTableModel tableModel=(DefaultTableModel) tabView1.getModel();
            tableModel.setRowCount(0);
            for(Loan loan : GetAllLoans){
                Employee Search_Employee_by_id = new Empolyee_Controller().Search_Employee_by_id(loan.getEid());
                if (Search_Employee_by_id != null) {
                    Object[] row = {loan.getLid(),Search_Employee_by_id.getLastname(),loan.getAmount(),loan.getValid_Preiod(),loan.getNo_of_Instalments(),loan.getInterest_Rate()};
                    tableModel.addRow(row);
                } else {
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(View_All_Loans.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void getValue() {
        int count = 0;
        try {
            int selectedRow = tabView1.getSelectedRow();
            String Lid = tabView1.getValueAt(selectedRow, 0).toString();
            String noOf = tabView1.getValueAt(selectedRow, 4).toString();
            Loan searchLoans_byloanid = new Loan_Controller().searchLoans_byloanid(Lid);
            if (searchLoans_byloanid != null) {
                Department_Detail searchDepartment_Detailseid = new Department_Details_Controller().searchDepartment_Detailseid(searchLoans_byloanid.getEid());
                if (searchDepartment_Detailseid != null) {
                    Department search_Department_by_id = new Department_Controller().search_Department_by_id(searchDepartment_Detailseid.getDid());
                    if (search_Department_by_id != null) {
                        ArrayList<Loan_Payment> searchLoanPayment_byLoanid = new Loan_Payment_Controller().searchLoanPayment_byLoanid(Lid);
                        for (Loan_Payment loan_Payment : searchLoanPayment_byLoanid) {
                            if (loan_Payment.getPay() == 1) {
                                count++;
                            }
                        }
                        texPayInsta.setText(String.valueOf(count));
                        texBalanceInst.setText(String.valueOf(Integer.parseInt(noOf) - count));
                        texDepartment.setText(search_Department_by_id.getName());
                    } else {
                        
                    }
                } else {
                    
                }
            } else {
                
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(View_All_Loans.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
