    private void inputGrade() {
        String id = texid.getText();
        String name = texName.getText();
        String age = texAge.getText();
        Job_Role job_Role=new Job_Role(id, name, age);
        Jobrole_Controller controller=new Jobrole_Controller();
        try {
            int addJobRole = controller.addJobRole(job_Role);
            if (addJobRole > 0) {
                JOptionPane.showMessageDialog(this, "\n\tJob Role Input Success !!!");
                texName.setText("");
                texAge.setText("");
                loadid();
                Object [] row={job_Role.getName(),job_Role.getPension_Age()};
                model.addRow(row);
            } else {
                JOptionPane.showMessageDialog(this, "\n\tjob Role Input Fail !!!\n Try Again !!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Manage_Job_Role_info.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setdate() {
        Date date=new Date();
        SimpleDateFormat dateFormat=new SimpleDateFormat("YYYY-MM-DD");
        String format = dateFormat.format(date);
        texDate.setText(format);
    }

    private void loadid() {
        try {
            String newId = IdGenerator.getNewId("Job_Role", "Jrid", "Jr");
            texid.setText(newId);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(input_jobrole.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
