    private void loadGrade() {
     try {
         ArrayList<Grade> allgrades = new Grade_Controller().getAllgrades();
         DefaultComboBoxModel model=(DefaultComboBoxModel) comboGradeName.getModel();
         model.removeAllElements();
         for(Grade grade : allgrades){;
             model.addElement(grade.getName());
         }
         AutoCompleteDecorator.decorate(comboGradeName);
     } catch (ClassNotFoundException | SQLException ex) {
         Logger.getLogger(search_grade.class.getName()).log(Level.SEVERE, null, ex);
     }
    }

