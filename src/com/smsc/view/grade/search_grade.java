    private void updateGrade() {
        String date = texDate.getText();
        String name = texName.getText();
        double basicsalary = Double.parseDouble(texBasicSalary.getText());
        String dalyworking = texDalyworking.getText();
        String id = texGradeid.getText();
        String halfDay = texHalfday.getText();
        String leaves = texLeaves.getText();
        double maxLoan = Double.parseDouble(texMaxLoanAmount.getText());
        String nopay = texNopay.getText();
        double nopayfee = Double.parseDouble(txtnopayfee.getText());
        String traning = texTraining.getText();
        double traningfee = Double.parseDouble(texTrainingPreiodFee.getText());
        double transport = Double.parseDouble(texTransportAmount.getText());
        String weekdayot = texWeekDayOt.getText();
        double weekdayOtfee = Double.parseDouble(texWeekdayOtfee.getText());
        String weekendot = texWeekendOt.getText();
        double weekendOtfee = Double.parseDouble(texWeekendotFee.getText());
        
        Grade grade=new Grade(id, name, date, basicsalary, dalyworking, maxLoan, transport, traning, traningfee, nopay, nopayfee, weekendot, weekendOtfee, weekdayot, weekdayOtfee, leaves, leaves);
     try {
            int updateGrade = new Grade_Controller().updateGrade(grade);
            if (updateGrade > 0) {
                JOptionPane.showMessageDialog(this, "Grade Update Success !!!");
                texaddDate.setText("");
                texName.setText("");
                texBasicSalary.setText("");
                texDalyworking.setText("");
                texGradeid.setText("");
                texHalfday.setText("");
                texLeaves.setText("");
                texMaxLoanAmount.setText("");
                texNopay.setText("");
                txtnopayfee.setText("");
                texTraining.setText("");
                texTrainingPreiodFee.setText("");
                texTransportAmount.setText("");
                texWeekDayOt.setText("");
                texWeekdayOtfee.setText("");
                texWeekendOt.setText("");
                texWeekendotFee.setText("");
         } else {
             int x=0;
             for (int i = 0; i < 11; i++) {
                x=x+i;
             }
             while(x==20){
                this.setCursor(new Cursor(WAIT_CURSOR));
             }   
             JOptionPane.showMessageDialog(this, "Grade Update fail !!!\n Please Check You Enter Details and Try Again !!!");
         }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(search_grade.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

