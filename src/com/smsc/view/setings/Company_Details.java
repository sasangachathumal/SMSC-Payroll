    private void addDetailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDetailsButtonActionPerformed
        try {
            if (texCompanyName.getText() == null || texCompanyAddress.getText() == null && texTelephoneNo.getText() == null && texFaxNo.getText() == null && texEmail.getText() == null && texRegesterNo.getText() == null) {
                texCompanyName.setBorder(new LineBorder(Color.red, 2));
                texCompanyAddress.setBorder(new LineBorder(Color.red, 2));
                texTelephoneNo.setBorder(new LineBorder(Color.red, 2));
                texFaxNo.setBorder(new LineBorder(Color.red, 2));
                texEmail.setBorder(new LineBorder(Color.red, 2));
                texRegesterNo.setBorder(new LineBorder(Color.red, 2));
            }
            if (texCompanyName.getText() != null) {
                name = texCompanyName.getText();
            } else {
                texCompanyName.setBackground(Color.red);
            }
            if (texCompanyAddress.getText() != null) {
                address = texCompanyAddress.getText();
            } else {
                texCompanyAddress.setBackground(Color.red);
            }
            if (texTelephoneNo.getText() != null) {
                telephone = texTelephoneNo.getText();
            } else {
                texTelephoneNo.setBackground(Color.red);
            }
            if (texFaxNo.getText() != null) {
                fax = texFaxNo.getText();
            } else {
                texFaxNo.setBackground(Color.red);
            }
            if (texEmail.getText() != null) {
                email = texEmail.getText();
            } else {
                texEmail.setBackground(Color.red);
            }
            if (texRegesterNo.getText() != null) {
                regesterNo = texRegesterNo.getText();
            } else {
                texRegesterNo.setBackground(Color.red);
            }
            
            int Input_Company_Details = new conpany_details().Input_Company_Details(id, name, address, telephone, fax, email, regesterNo);
            if (Input_Company_Details==1) {
                 new Employee_Expenses().setVisible(true);
                 this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "jkhoiyhgnhjhdfjl");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Company_Details.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addDetailsButtonActionPerformed

