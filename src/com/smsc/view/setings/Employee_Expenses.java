    private void btInputEmployeeExpetionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInputEmployeeExpetionActionPerformed
        try {
            String EEid="EE001";
            double epfEmployee = Double.parseDouble(texEPFEmployee.getText());
            double epfCompany = Double.parseDouble(texEPFCompany.getText());
            double etf = Double.parseDouble(texETF.getText());
            double attendance = Double.parseDouble(texAttendance.getText());
            double promotion = Double.parseDouble(texPromotion.getText());
            String Date = texDate.getText();
            
            Employee_Expetions employee_Expetions=new Employee_Expetions(EEid, epfEmployee, epfCompany, etf, attendance, promotion, Date,"");
            
            Employee_Expetions_Controller employee_Expetions_Controller=new Employee_Expetions_Controller();
            int addEmployee_Expetion = employee_Expetions_Controller.addEmployee_Expetion(employee_Expetions);
            if (addEmployee_Expetion > 0) {
                JOptionPane.showMessageDialog(this, "Employee Expetion Input Scuccess !!!");
            } else {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Employee Exprtion Input Fail !!!");
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Employee_Expenses.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btInputEmployeeExpetionActionPerformed

