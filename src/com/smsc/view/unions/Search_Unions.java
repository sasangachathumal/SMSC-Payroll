    private void loadData() {
        try {
            ArrayList<Unions> all = new Union_Controller().getAll();
            DefaultTableModel tableModel=(DefaultTableModel) tabView.getModel();
            tableModel.setRowCount(0);
            for(Unions unions : all){
                Object [] row={unions.getName(),unions.getOpenDate(),unions.getMSFee(),unions.getRegister_No()};
                tableModel.addRow(row);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Unions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void getValue() {
        selectedRow = tabView.getSelectedRow();
        String name = tabView.getValueAt(selectedRow, 0).toString();
        try {
            Unions searchUnions_byname = new Union_Controller().searchUnions_byname(name);
            if (searchUnions_byname != null) {
                UID=searchUnions_byname.getUid();
                texName.setText(searchUnions_byname.getName());
                texFee.setText(String.valueOf(searchUnions_byname.getMSFee()));
                texOpenDate.setText(searchUnions_byname.getOpenDate());
                texRegister.setText(searchUnions_byname.getRegister_No());
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Unions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ifUpdate() {
        if (selectedRow > -1) {
            int showConfirmDialog = JOptionPane.showConfirmDialog(this, "Are You want to Update This ");
            if (showConfirmDialog == JOptionPane.YES_OPTION) {
                updateUnion();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Plese select a row ");
        }
    }

    private void updateUnion() {
        try {
            String name = texName.getText();
            double fee = Double.parseDouble(texFee.getText());
            String opdate = texOpenDate.getText();
            String reno = texRegister.getText();
            
            Unions unions=new Unions(UID, name, opdate, fee, reno);
            int updateUnion = new Union_Controller().updateUnion(unions);
            if (updateUnion > 0) {
                JOptionPane.showMessageDialog(this, "Union Update Success ");
                texFee.setText("");
                texName.setText("");
                texOpenDate.setText("");
                texRegister.setText("");
                UID="";
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Union Update Fail  Please check and try again");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Unions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ifRemove() {
        if (selectedRow > -1) {
            int showConfirmDialog = JOptionPane.showConfirmDialog(this, "Are You want to Remove This ");
            if (showConfirmDialog == JOptionPane.YES_OPTION) {
                removeUnion();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Plese select a row ");
        }
    }

    private void removeUnion() {
        try {
            int Removeunion = new Union_Controller().Removeunion(UID);
            if (Removeunion > 0) {
                JOptionPane.showMessageDialog(this, "Union Remove Success ");
                texFee.setText("");
                texName.setText("");
                texOpenDate.setText("");
                texRegister.setText("");
                UID="";
                loadData();
            }else {
                JOptionPane.showMessageDialog(this, "Union Remove Fail  Please check and try again");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Unions.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
