    private void markattendanceInmorning() {
        String id = Textattendanceid1.getText();
        String employeenic=String.valueOf(ComboEmployeeNic.getSelectedItem());
        String date=Texdate.getText();
        String DateType;
        if (CheckBoxholiday.isSelected()) {
            DateType="Holiday";
        } else {
            DateType="null";
        }
        String attend=String.valueOf(ComboEmployeeAttend.getSelectedItem());
        String arrivaltime=TextArrivaltime.getText();
        String hour = Arrays.toString(arrivaltime.split(":",0));
        try {
            int mark_attendance_in_morning = new Attendance_Calculation().mark_attendance_in_morning(id, employeenic, date, DateType, attend, hour);
            if(mark_attendance_in_morning !=10 && mark_attendance_in_morning ==1){
                JOptionPane.showMessageDialog(this, "Attendance Mark success !!!");
                TextArrivaltime.setText("");
            } else {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Attendance Mark Fail !!!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Mark_Attendance.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void getOTandleave() {
        if (time1 != null) {
            TextLeavetime.setText(time1);
        } else {
            btLeaveTime.setCursor(new Cursor(WAIT_CURSOR));
        }
        if (TextLeavetime.getText() != null) {
            String hour = Arrays.toString(TextLeavetime.getText().split(":", 0));
            try {
                String _Attendance_ID = new Attendance_Calculation().get_Attendance_ID(String.valueOf(ComboEmployeeNic1.getSelectedItem()), Texdate.getText());
                double Create_OT_Bonus = new Attendance_Calculation().Create_OT_Bonus(_Attendance_ID, hour, String.valueOf(ComboEmployeeNic1.getSelectedItem()));
                if (Create_OT_Bonus != 0.0 && Create_OT_Bonus != 10.0 && Create_OT_Bonus != 100.0) {
                    TextOtamount.setText(String.valueOf(Create_OT_Bonus));
                } else {
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, "No OT Bonus to Calculate !!!");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(Mark_Attendance.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Error in get time !!!");
        }
    }

    private void getAMPM() {
        String attend=ComboEmployeeAttend.getSelectedItem().toString();
        if (attend.equals("Absent")) {
            btGetArrivalTime.setEnabled(false);
            btLeaveTime.setEnabled(false);
        } else {
            btGetArrivalTime.setEnabled(true);
            btLeaveTime.setEnabled(true);
        }
    }

    private void updateTodayattendance() {
        try {
            String leavetime = TextLeavetime.getText();
            double otamount = Double.parseDouble(TextOtamount.getText());
            Attendance attendance=new Attendance(leavetime,otamount);
            int update_Attendance_LeaveTome_Ot = Attendance_Contorller.update_Attendance_LeaveTome_Ot(attendance);
            if (update_Attendance_LeaveTome_Ot > 0) {
                JOptionPane.showMessageDialog(this, "Attendance Update Scuccess !!!");
                TextArrivaltime1.setText("");
                TextLeavetime.setText("");
                TextOtamount.setText("");
                texAttend.setText("");
            } else {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Attendance Update Fail !!!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Mark_Attendance.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

