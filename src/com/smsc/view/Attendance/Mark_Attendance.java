        private void setTime(){
        new Thread()
        {
            @Override
            public void run()
            {
                while(timeRun==0){
                    Calendar cal=new GregorianCalendar();
                    int AM_PM=cal.get(Calendar.AM_PM);
                    String day="";
                    if(AM_PM==1)
                    {
                    day="PM";
                    }
                    else 
                    {
                        day="AM";
                    }
                    Date d=new Date();
                    SimpleDateFormat dateFormat1=new SimpleDateFormat("HH:mm:ss:aa");
                    String format1 = dateFormat1.format(d);
                    labTime.setText(format1);
                    time1=format1;
                    if (day.equals("AM")) {
                        btGetArrivalTime.setEnabled(true);
                        btLeaveTime.setEnabled(false);
                    } else {
                        btLeaveTime.setEnabled(true);
                        btGetArrivalTime.setEnabled(false);
                    }
                    }
               }
         }.start();
    }
    public void loadEmployeeNIC() throws ClassNotFoundException, SQLException{
        dcbm=(DefaultComboBoxModel) ComboEmployeeNic.getModel();
        dcbm1=(DefaultComboBoxModel) ComboEmployeeNic1.getModel();
        dcbm.removeAllElements();
        dcbm.addElement("");
        dcbm1.removeAllElements();
        dcbm1.addElement("");
        ArrayList<Employee> _all_Employee = empolyee_Controller.get_all_Employee();
        for(Employee employee:_all_Employee){
            dcbm.addElement(employee.getNic_No());
            AutoCompleteDecorator.decorate(ComboEmployeeNic);
            dcbm1.addElement(employee.getNic_No());
            AutoCompleteDecorator.decorate(ComboEmployeeNic1);
        }
    }
