    public void loadBonusName() {
        DefaultComboBoxModel dcbm=(DefaultComboBoxModel) comboBonusName.getModel();
        try {
            ArrayList<Bonus> allBonus = new Bonus_Controller().getallBonus();
            if (!allBonus.isEmpty()) {
                for(Bonus bonus : allBonus){
                    dcbm.addElement(bonus.getName());
                }
            } else {
                System.out.println("jkfghdfgjkdfg");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddBonusAmount.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setvalue() {
        String name = comboBonusName.getSelectedItem().toString();
        double fee = Double.parseDouble(texBonusFee.getText());
        Amount=Amount+fee;
        Object [] row={name,fee};
        model.addRow(row);
        texAmount.setText(String.valueOf(Amount));
    }

    private void setFee() {
        try {
            String name=comboBonusName.getSelectedItem().toString();
            Bonus bonus= new Bonus_Controller().searchbonus_bybonusname(name);
            if (bonus != null) {
                texBonusFee.setText(String.valueOf(bonus.getAmount()));
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddBonusAmount.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ifRemove() {
        if (select > -1) {
            model.removeRow(select);
        } else {
            JOptionPane.showMessageDialog(this, "Please Select a Row !!!");
        }
    }

    private void sentValue() {
        int count1=0;
        int count=tabView.getRowCount();
        for (int i = 0; i < count; i++) {
            new Salary().setBonus(Double.parseDouble(String.valueOf(tabView.getValueAt(i, 1))));
            count1++;
        }
        if (count==count1) {
            new Manage_Salary().bonus();
            this.dispose();
        }
        //Salary.setBonus(Double.parseDouble(texAmount.getText()));
    }
